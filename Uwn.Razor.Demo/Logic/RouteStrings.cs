	public static string DetermineRoute<TEnum>() where TEnum : Enum
	{
		Type type = typeof(TEnum);
		if (type == typeof(Enumerations.AbsoluteLengthUnit)) return EnumerationsLengthUnits;
		if (type == typeof(Enumerations.RelativeLengthUnit)) return EnumerationsLengthUnits;
		if (type == typeof(Enumerations.Bootstrap.Decorations)) return EnumerationsBootstrapDecorations;
		if (type == typeof(Enumerations.Bootstrap.ModalFullscreenMode)) return EnumerationsBootstrapModalFullscreenMode;
		if (type == typeof(Enumerations.Bootstrap.ModalSize)) return EnumerationsBootstrapModalSize;
		if (type == typeof(Enumerations.Bootstrap.Placement)) return EnumerationsBootstrapPlacement;
		if (type == typeof(Enumerations.Bootstrap.Position)) return EnumerationsBootstrapPosition;
		if (type == typeof(Enumerations.Bootstrap.Style)) return EnumerationsBootstrapStyle;
		if (type == typeof(Enumerations.Bootstrap.StyleUsages)) return EnumerationsBootstrapStyleUsages;
		if (type == typeof(Enumerations.Bootstrap.TablePurpose)) return EnumerationsBootstrapTablePurpose;
		if (type == typeof(Enumerations.FontAwesome.IconPacks)) return EnumerationsFontAwesomeIconPacks;
		if (type == typeof(Enumerations.FontAwesome.Styles)) return EnumerationsFontAwesomeStyles;
		return String.Empty;
	}
